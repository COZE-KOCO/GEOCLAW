/**
 * 根据内容推荐模板
 */
export function recommendTemplate(title: string, content: string): {
  template: ContentTemplate;
  confidence: number;
  reason: string;
} {
  // 问答型判断
  if (/^(如何|怎么|为什么|什么是|哪个)/.test(title)) {
    return {
      template: qnaTemplate,
      confidence: 0.85,
      reason: '标题为问句形式，适合问答型模板',
    };
  }

  // 对比型判断
  if (title.includes('对比') || title.includes('VS') || title.includes('vs') || title.includes('哪个好')) {
    return {
      template: comparisonTemplate,
      confidence: 0.85,
      reason: '标题包含对比关键词，适合对比型模板',
    };
  }

  // 指南型判断
  if (title.includes('指南') || title.includes('教程') || title.includes('步骤') || title.includes('方法')) {
    return {
      template: guideTemplate,
      confidence: 0.8,
      reason: '标题包含指导性关键词，适合操作指南模板',
    };
  }

  // 案例型判断
  if (title.includes('案例') || title.includes('实践') || title.includes('故事')) {
    return {
      template: caseTemplate,
      confidence: 0.75,
      reason: '标题包含案例关键词，适合案例型模板',
    };
  }

  // 报告型判断
  if (title.includes('报告') || title.includes('趋势') || title.includes('分析')) {
    return {
      template: reportTemplate,
      confidence: 0.8,
      reason: '标题包含分析关键词，适合行业报告模板',
    };
  }

  // 默认推荐问答型
  return {
    template: qnaTemplate,
    confidence: 0.5,
    reason: '问答型模板AI引用率最高，建议优先使用',
  };
}
